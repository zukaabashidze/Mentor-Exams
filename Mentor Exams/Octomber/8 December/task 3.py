def reverse(text):
    return ' '.join(text.split()[::-1]) # სიტყვები დავაბრუნე და დავუკავშირე ერთმანეთს როგორც ცოლ-ქმარი

print(reverse("Hello World"))
print(reverse("Python is great"))
print(reverse("a b c"))
print(reverse(""))
print(reverse( " Spaces "))

